package szpital.encje.pacjenci;

public class Adres {
	
	private String ulica;
	private String numerDomu;
	private String numerMieszkania;
	private String kodPoczotwy;
	private String miasto;
	public String getUlica() {
		return ulica;
	}
	public void setUlica(String ulica) {
		this.ulica = ulica;
	}
	public String getNumerDomu() {
		return numerDomu;
	}
	public void setNumerDomu(String numerDomu) {
		this.numerDomu = numerDomu;
	}
	public String getNumerMieszkania() {
		return numerMieszkania;
	}
	public void setNumerMieszkania(String numerMieszkania) {
		this.numerMieszkania = numerMieszkania;
	}
	public String getKodPoczotwy() {
		return kodPoczotwy;
	}
	public void setKodPoczotwy(String kodPoczotwy) {
		this.kodPoczotwy = kodPoczotwy;
	}
	public String getMiasto() {
		return miasto;
	}
	public void setMiasto(String miasto) {
		this.miasto = miasto;
	}
	
	

}
